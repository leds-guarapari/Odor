/**
	* 
	* Default odor class data.
	* 
	*/
export class Odor {

	/**
		* @param {string} id
		*/
	set id(id) {
		this._id = id;
	}

	/**
		* @returns {string} id
		*/
	get id() {
		return this._id;
	}

	/**
		* @param {string} userid
		*/
	set userid(userid) {
		this._userid = userid;
	}

	/**
		* @returns {string} userid
		*/
	get userid() {
		return this._userid;
	}

	/**
		* @param {string} username
		*/
	set username(username) {
		this._username = username;
	}

	/**
		* @returns {string} username
		*/
	get username() {
		return this._username;
	}

	/**
		* @param {string} usertype
		*/
	set usertype(usertype) {
		this._usertype = usertype;
	}

	/**
		* @returns {string} usertype
		*/
	get usertype() {
		return this._usertype;
	}

	/**
		* @param {string} userorigin
		*/
	set userorigin(userorigin) {
		this._userorigin = userorigin;
	}

	/**
		* @returns {string} userorigin
		*/
	get userorigin() {
		return this._userorigin;
	}

	/**
		* @param {string} intensity
		*/
	set intensity(intensity) {
		this._intensity = intensity;
	}

	/**
		* @returns {string} intensity
		*/
	get intensity() {
		return this._intensity;
	}

	/**
		* @param {string} nuisance
		*/
	set nuisance(nuisance) {
		this._nuisance = nuisance;
	}

	/**
		* @returns {string} nuisance
		*/
	get nuisance() {
		return this._nuisance;
	}

	/**
		* @param {string} type
		*/
	set type(type) {
		this._type = type;
	}

	/**
		* @returns {string} type
		*/
	get type() {
		return this._type;
	}

	/**
		* @param {string} origin
		*/
	set origin(origin) {
		this._origin = origin;
	}

	/**
		* @returns {string} origin
		*/
	get origin() {
		return this._origin;
	}

	/**
		* @param {string} address
		*/
	set address(address) {
		this._address = address;
	}

	/**
		* @returns {string} address
		*/
	get address() {
		return this._address;
	}

	/**
		* @param {number} latitude
		*/
	set latitude(latitude) {
		this._latitude = latitude;
	}

	/**
		* @returns {number} latitude
		*/
	get latitude() {
		return this._latitude;
	}

	/**
		* @param {number} longitude
		*/
	set longitude(longitude) {
		this._longitude = longitude;
	}

	/**
		* @returns {number} longitude
		*/
	get longitude() {
		return this._longitude;
	}

	/**
		* @param {datetime} date
		*/
	set date(date) {
		this._date = date;
	}

	/**
		* @returns {datetime} date
		*/
	get date() {
		return this._date;
	}

	/**
		* @param {timespan} begin
		*/
	set begin(begin) {
		this._begin = begin;
	}

	/**
		* @returns {timespan} begin
		*/
	get begin() {
		return this._begin;
	}

	/**
		* @param {timespan} end
		*/
	set end(end) {
		this._end = end;
	}

	/**
		* @returns {timespan} end
		*/
	get end() {
		return this._end;
	}

}